async function renderElementToPng(element, pixelRatio = 2) {
    const svgUrl = await renderElementToSvg(element, { requireEmbeddedAssets: true });
    const image = await loadImage(svgUrl);
    if (!image) throw new Error('無法建立籤詩圖片');

    const rect = element.getBoundingClientRect();
    const canvas = document.createElement('canvas');
    const ctx = canvas.getContext('2d');
    canvas.width = Math.round(rect.width * pixelRatio);
    canvas.height = Math.round(rect.height * pixelRatio);
    ctx.drawImage(image, 0, 0, canvas.width, canvas.height);
    URL.revokeObjectURL(svgUrl);
    return canvas.toDataURL('image/png');
}

async function renderElementToSvg(element, options = {}) {
    const rect = element.getBoundingClientRect();
    const clone = element.cloneNode(true);
    inlineComputedStyles(element, clone);

    clone.style.width = `${rect.width}px`;
    clone.style.height = `${rect.height}px`;
    clone.style.margin = '0';
    clone.style.transform = 'none';

    await embedExportImages(clone, options);
    await embedBackgroundImages(clone, options);

    const wrapper = document.createElement('div');
    wrapper.setAttribute('xmlns', 'http://www.w3.org/1999/xhtml');
    wrapper.style.width = `${rect.width}px`;
    wrapper.style.height = `${rect.height}px`;
    wrapper.appendChild(clone);

    const svg = document.createElementNS('http://www.w3.org/2000/svg', 'svg');
    svg.setAttribute('xmlns', 'http://www.w3.org/2000/svg');
    svg.setAttribute('width', String(rect.width));
    svg.setAttribute('height', String(rect.height));
    svg.setAttribute('viewBox', `0 0 ${rect.width} ${rect.height}`);

    const foreignObject = document.createElementNS('http://www.w3.org/2000/svg', 'foreignObject');
    foreignObject.setAttribute('width', '100%');
    foreignObject.setAttribute('height', '100%');
    foreignObject.appendChild(wrapper);
    svg.appendChild(foreignObject);

    const markup = new XMLSerializer().serializeToString(svg);
    return URL.createObjectURL(new Blob([markup], { type: 'image/svg+xml;charset=utf-8' }));
}

function inlineComputedStyles(source, target) {
    const computedStyle = window.getComputedStyle(source);
    target.style.cssText = Array.from(computedStyle)
        .map(property => `${property}:${computedStyle.getPropertyValue(property)};`)
        .join('');

    Array.from(source.children).forEach((sourceChild, index) => {
        inlineComputedStyles(sourceChild, target.children[index]);
    });
}

async function embedExportImages(root, options = {}) {
    const images = Array.from(root.querySelectorAll('img'));
    await Promise.all(images.map(async image => {
        const dataUrl = await urlToDataUrl(image.currentSrc || image.src);
        if (dataUrl) {
            image.src = dataUrl;
            return;
        }

        if (options.requireEmbeddedAssets) throw new Error('歌曲封面無法內嵌到 PNG');
    }));
}

async function embedBackgroundImages(root, options = {}) {
    const elements = [root, ...root.querySelectorAll('*')];
    await Promise.all(elements.map(async element => {
        const bgUrl = extractBackgroundUrl(element.style.backgroundImage);
        if (!bgUrl) return;

        const dataUrl = await urlToDataUrl(bgUrl);
        if (dataUrl) {
            element.style.backgroundImage = `url("${dataUrl}")`;
            return;
        }

        if (options.requireEmbeddedAssets) throw new Error('背景書籤無法內嵌到 PNG');
    }));
}

async function urlToDataUrl(url) {
    if (!url || url.startsWith('data:') || url.startsWith('blob:')) return url;

    try {
        const response = await fetch(url, { mode: 'cors' });
        if (!response.ok) return '';

        const blob = await response.blob();
        return await blobToDataUrl(blob);
    } catch (error) {
        return '';
    }
}

function blobToDataUrl(blob) {
    return new Promise((resolve, reject) => {
        const reader = new FileReader();
        reader.onload = () => resolve(reader.result);
        reader.onerror = reject;
        reader.readAsDataURL(blob);
    });
}

function downloadDataUrl(dataUrl, filename) {
    const link = document.createElement('a');
    link.href = dataUrl;
    link.download = filename;
    document.body.appendChild(link);
    link.click();
    link.remove();
}

function initMusicSettings() {
    if (!bgMusic) return;

    const initialVolume = DEFAULT_VOLUME;
    bgMusic.volume = initialVolume;
    bgMusic.muted = false;
    localStorage.setItem('btsMusicVolume', String(initialVolume));
    localStorage.setItem('btsMusicMuted', 'false');

    if (volumeSlider) {
        volumeSlider.value = bgMusic.volume;
        volumeSlider.addEventListener('input', () => {
            bgMusic.volume = Number(volumeSlider.value);
            bgMusic.muted = bgMusic.volume === 0;
            localStorage.setItem('btsMusicVolume', String(bgMusic.volume));
            localStorage.setItem('btsMusicMuted', String(bgMusic.muted));
            updateMuteLabel();
        });
    }

    if (settingsToggle) {
        settingsToggle.addEventListener('click', () => {
            if (bgMusic.muted || bgMusic.volume === 0) {
                bgMusic.muted = false;
                bgMusic.volume = DEFAULT_VOLUME;
            } else {
                bgMusic.muted = true;
                bgMusic.volume = 0;
            }

            if (volumeSlider) volumeSlider.value = bgMusic.volume;
            localStorage.setItem('btsMusicVolume', String(bgMusic.volume));
            localStorage.setItem('btsMusicMuted', String(bgMusic.muted));
            updateMuteLabel();
        });
    }

    updateMuteLabel();
}

function updateMuteLabel() {
    if (!bgMusic) return;
    const isMuted = bgMusic.muted || bgMusic.volume === 0;
    if (settingsToggle) {
        settingsToggle.classList.toggle('is-muted', isMuted);
        settingsToggle.setAttribute('aria-label', isMuted ? '取消靜音' : '靜音');
    }
    if (volumeSlider && Number(volumeSlider.value) !== bgMusic.volume) {
        volumeSlider.value = bgMusic.volume;
    }
}

