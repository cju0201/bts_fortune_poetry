function preloadImage(src, timeoutMs = 5000) {
    return new Promise(resolve => {
        if (!src) {
            resolve(false);
            return;
        }

        const image = new Image();
        let settled = false;
        const done = ok => {
            if (settled) return;
            settled = true;
            clearTimeout(timer);
            resolve(ok);
        };
        const timer = setTimeout(() => done(false), timeoutMs);

        image.onload = () => done(true);
        image.onerror = () => done(false);
        image.src = src;
    });
}

