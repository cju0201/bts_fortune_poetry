function scaledRect(selector, cardRect, scaleX, scaleY) {
    const el = document.querySelector(selector);
    if (!el) return { x: 0, y: 0, w: 0, h: 0 };
    const rect = el.getBoundingClientRect();
    return {
        x: round((rect.left - cardRect.left) * scaleX),
        y: round((rect.top - cardRect.top) * scaleY),
        w: round(rect.width * scaleX),
        h: round(rect.height * scaleY)
    };
}

function svgButton(rect, label, color) {
    if (!rect.w || !rect.h) return '';
    const fontSize = rect.w < 88 ? 12 : 15;
    return `<rect x="${rect.x}" y="${rect.y}" width="${rect.w}" height="${rect.h}" rx="${rect.h / 2}" fill="${color}"/>
  <text x="${rect.x + rect.w / 2}" y="${rect.y + rect.h / 2 + fontSize / 3}" fill="#ffffff" font-family="Microsoft JhengHei, PingFang TC, sans-serif" font-size="${fontSize}" font-weight="700" text-anchor="middle">${escapeXml(label)}</text>`;
}

function svgWrappedText(text, x, y, width, fontSize, color, lineHeightPx, lineHeightRatio, maxLines, weight = 400, style = 'normal', anchor = 'start') {
    const charsPerLine = Math.max(6, Math.floor(width / (fontSize * 0.95)));
    const lines = wrapText(text, charsPerLine, maxLines);
    const textX = anchor === 'middle' ? x + width / 2 : x;
    const tspans = lines.map((line, index) => {
        const dy = index === 0 ? 0 : lineHeightPx * lineHeightRatio;
        return `<tspan x="${textX}" dy="${index === 0 ? 0 : dy}">${escapeXml(line)}</tspan>`;
    }).join('');

    return `<text x="${textX}" y="${y}" fill="${color}" font-family="Microsoft JhengHei, PingFang TC, sans-serif" font-size="${fontSize}" font-weight="${weight}" font-style="${style}" text-anchor="${anchor}">${tspans}</text>`;
}

function wrapText(text, charsPerLine, maxLines) {
    const rawLines = String(text).replace(/[“”"]/g, '').split(/\n+/);
    const lines = [];

    rawLines.forEach(rawLine => {
        let current = rawLine.trim();
        while (current.length > charsPerLine && lines.length < maxLines) {
            lines.push(current.slice(0, charsPerLine));
            current = current.slice(charsPerLine);
        }
        if (current && lines.length < maxLines) lines.push(current);
    });

    if (lines.length === maxLines && rawLines.join('').length > lines.join('').length) {
        lines[maxLines - 1] = `${lines[maxLines - 1].slice(0, Math.max(0, charsPerLine - 1))}…`;
    }

    return lines;
}

function extractBackgroundUrl(backgroundImage) {
    const match = backgroundImage.match(/url\(["']?(.*?)["']?\)/);
    return match ? match[1] : '';
}

function textOf(selector) {
    return document.querySelector(selector)?.innerText.trim() || '';
}

function safeFilename(text) {
    return String(text).replace(/[\\/:*?"<>|]/g, '-').slice(0, 40);
}

function escapeXml(text) {
    return String(text)
        .replace(/&/g, '&amp;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;')
        .replace(/"/g, '&quot;');
}

function escapeHtml(text) {
    return escapeXml(text).replace(/'/g, '&#39;');
}

function escapeAttribute(text) {
    return escapeHtml(text);
}

function round(value) {
    return Math.round(value * 100) / 100;
}

