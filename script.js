function initNightSky() {
    const canvas = document.getElementById('night-sky');
    if (!canvas) return;

    const ctx = canvas.getContext('2d');
    const stars = [];
    const starCount = 140;

    function resize() {
        canvas.width = window.innerWidth * window.devicePixelRatio;
        canvas.height = window.innerHeight * window.devicePixelRatio;
        canvas.style.width = `${window.innerWidth}px`;
        canvas.style.height = `${window.innerHeight}px`;
        ctx.setTransform(window.devicePixelRatio, 0, 0, window.devicePixelRatio, 0, 0);
    }

    function seedStars() {
        stars.length = 0;
        for (let i = 0; i < starCount; i += 1) {
            stars.push({
                x: Math.random() * window.innerWidth,
                y: Math.random() * window.innerHeight,
                r: Math.random() * 1.6 + 0.25,
                speed: Math.random() * 0.18 + 0.04,
                alpha: Math.random() * 0.65 + 0.25
            });
        }
    }

    function draw() {
        ctx.clearRect(0, 0, window.innerWidth, window.innerHeight);
        const glow = ctx.createRadialGradient(
            window.innerWidth * 0.52,
            window.innerHeight * 0.36,
            0,
            window.innerWidth * 0.52,
            window.innerHeight * 0.36,
            Math.max(window.innerWidth, window.innerHeight) * 0.46
        );
        glow.addColorStop(0, 'rgba(167, 98, 255, 0.22)');
        glow.addColorStop(1, 'rgba(167, 98, 255, 0)');
        ctx.fillStyle = glow;
        ctx.fillRect(0, 0, window.innerWidth, window.innerHeight);

        stars.forEach(star => {
            star.y += star.speed;
            if (star.y > window.innerHeight + 4) {
                star.y = -4;
                star.x = Math.random() * window.innerWidth;
            }
            ctx.beginPath();
            ctx.fillStyle = `rgba(255,255,255,${star.alpha})`;
            ctx.arc(star.x, star.y, star.r, 0, Math.PI * 2);
            ctx.fill();
        });

        requestAnimationFrame(draw);
    }

    resize();
    seedStars();
    draw();
    window.addEventListener('resize', () => {
        resize();
        seedStars();
    });
}

function scaledRectForPng(selector, cardRect, scaleX, scaleY) {
    const el = document.querySelector(selector);
    if (!el) return { x: 0, y: 0, w: 0, h: 0 };
    const rect = el.getBoundingClientRect();
    return {
        x: (rect.left - cardRect.left) * scaleX,
        y: (rect.top - cardRect.top) * scaleY,
        w: rect.width * scaleX,
        h: rect.height * scaleY
    };
}

function scaledTextStyle(selector, scaleY) {
    const el = document.querySelector(selector);
    const style = el ? getComputedStyle(el) : null;
    const fontSize = style ? parseFloat(style.fontSize) * scaleY : 24;
    const lineHeightRaw = style?.lineHeight || '';
    const lineHeight = lineHeightRaw === 'normal'
        ? fontSize * 1.35
        : (parseFloat(lineHeightRaw) || fontSize * 1.35) * scaleY;
    const letterSpacingRaw = style?.letterSpacing || '0';
    const letterSpacing = letterSpacingRaw === 'normal' ? 0 : (parseFloat(letterSpacingRaw) || 0) * scaleY;

    return {
        fontSize,
        lineHeight,
        letterSpacing,
        weight: style?.fontWeight || 400,
        style: style?.fontStyle || 'normal'
    };
}

function loadImage(src) {
    return new Promise(resolve => {
        if (!src) {
            resolve(null);
            return;
        }

        const img = new Image();
        if (/^https?:\/\//.test(src)) img.crossOrigin = 'anonymous';
        img.onload = () => resolve(img);
        img.onerror = () => resolve(null);
        img.src = src;
    });
}

function drawCardFallback(ctx, width, height) {
    const gradient = ctx.createLinearGradient(0, 0, 0, height);
    gradient.addColorStop(0, '#18072f');
    gradient.addColorStop(0.55, '#2b1450');
    gradient.addColorStop(1, '#080214');
    ctx.fillStyle = gradient;
    ctx.fillRect(0, 0, width, height);
}

function drawRoundedImage(ctx, img, x, y, w, h, radius) {
    ctx.save();
    roundedPath(ctx, x, y, w, h, radius);
    ctx.clip();

    const sourceRatio = img.width / img.height;
    const targetRatio = w / h;
    let sw = img.width;
    let sh = img.height;
    let sx = 0;
    let sy = 0;

    if (sourceRatio > targetRatio) {
        sw = img.height * targetRatio;
        sx = (img.width - sw) / 2;
    } else {
        sh = img.width / targetRatio;
        sy = (img.height - sh) / 2;
    }

    ctx.drawImage(img, sx, sy, sw, sh, x, y, w, h);
    ctx.restore();
}

function roundedPath(ctx, x, y, w, h, radius) {
    const r = Math.min(radius, w / 2, h / 2);
    ctx.beginPath();
    ctx.moveTo(x + r, y);
    ctx.lineTo(x + w - r, y);
    ctx.quadraticCurveTo(x + w, y, x + w, y + r);
    ctx.lineTo(x + w, y + h - r);
    ctx.quadraticCurveTo(x + w, y + h, x + w - r, y + h);
    ctx.lineTo(x + r, y + h);
    ctx.quadraticCurveTo(x, y + h, x, y + h - r);
    ctx.lineTo(x, y + r);
    ctx.quadraticCurveTo(x, y, x + r, y);
    ctx.closePath();
}

function drawCanvasButton(ctx, rect, label, color) {
    if (!rect.w || !rect.h) return;
    ctx.save();
    roundedPath(ctx, rect.x, rect.y, rect.w, rect.h, rect.h / 2);
    ctx.fillStyle = color;
    ctx.fill();
    ctx.fillStyle = '#fff';
    ctx.font = `700 ${Math.max(18, rect.h * 0.42)}px "Microsoft JhengHei", "PingFang TC", sans-serif`;
    ctx.textAlign = 'center';
    ctx.textBaseline = 'middle';
    ctx.fillText(label, rect.x + rect.w / 2, rect.y + rect.h / 2);
    ctx.restore();
}

function drawWrappedCanvasText(ctx, text, x, y, maxWidth, options) {
    const {
        color,
        fontSize,
        lineHeight,
        weight = 400,
        style = 'normal',
        align = 'left',
        maxLines = 4,
        boxHeight = 0,
        verticalAlign = 'top',
        letterSpacing = 0
    } = options;
    const content = String(text || '').trim();
    const font = `${style} ${weight} ${fontSize}px "Microsoft JhengHei", "PingFang TC", sans-serif`;
    const lines = wrapTextForCanvas(ctx, content, maxWidth, font, maxLines, letterSpacing);
    const totalHeight = fontSize + Math.max(0, lines.length - 1) * lineHeight;
    const startY = verticalAlign === 'middle' && boxHeight
        ? y + (boxHeight - totalHeight) / 2 + fontSize
        : y + fontSize;

    ctx.save();
    ctx.fillStyle = color;
    ctx.font = font;
    ctx.textAlign = align;
    ctx.textBaseline = 'alphabetic';
    const textX = align === 'center' ? x + maxWidth / 2 : x;
    lines.forEach((line, index) => {
        drawCanvasTextLine(ctx, line, textX, startY + index * lineHeight, letterSpacing, align);
    });
    ctx.restore();
}

function wrapTextForCanvas(ctx, text, maxWidth, font, maxLines, letterSpacing = 0) {
    ctx.save();
    ctx.font = font;
    const lines = [];
    let line = '';

    for (const char of text) {
        const next = line + char;
        if (measureCanvasText(ctx, next, letterSpacing) > maxWidth && line) {
            lines.push(line);
            line = char;
            if (lines.length === maxLines) break;
        } else {
            line = next;
        }
    }

    if (line && lines.length < maxLines) lines.push(line);
    if (lines.length === maxLines && text.length > lines.join('').length) {
        lines[maxLines - 1] = `${lines[maxLines - 1].slice(0, -1)}…`;
    }
    ctx.restore();
    return lines;
}

function measureCanvasText(ctx, text, letterSpacing = 0) {
    return ctx.measureText(text).width + Math.max(0, String(text).length - 1) * letterSpacing;
}

function drawCanvasTextLine(ctx, text, x, y, letterSpacing = 0, align = 'left') {
    if (!letterSpacing) {
        ctx.fillText(text, x, y);
        return;
    }

    const chars = Array.from(text);
    const width = measureCanvasText(ctx, text, letterSpacing);
    let currentX = align === 'center' ? x - width / 2 : x;

    chars.forEach(char => {
        ctx.fillText(char, currentX, y);
        currentX += ctx.measureText(char).width + letterSpacing;
    });
}
